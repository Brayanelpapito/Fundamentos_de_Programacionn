public class Programa11 {

    public static void main(String[] args) {
      int c;
      c=1;
       while (c<=100){
             System.out.print(c);
             System.out.print("\n ");
             c = c + 1;}
    
       
       while (c>=-100){
             System.out.print(c);
             System.out.print("\n ");
             c = c - 1;}
}
}
